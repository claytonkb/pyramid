val8 compress_mtf_encode(pyr_cache *this_pyr, val8 in){ // compress_mtf_encode#

    mword in_size = array8_size(this_pyr, in);
    char *result = (char*)_newstr(this_pyr, in_size, 0);
    char *cin = (char*)in;

    char *byte_table  = (char*)_newstr(this_pyr, MTF_BYTE_TABLE_SIZE, 0);
    char *index_table = (char*)_newstr(this_pyr, MTF_BYTE_TABLE_SIZE, 0);

    int table_index=0;

    memcpy(byte_table,  mtf_byte_table, MTF_BYTE_TABLE_SIZE);
    memcpy(index_table, mtf_byte_table, MTF_BYTE_TABLE_SIZE);

    int i;
    char c;

    for(i=0; i<in_size; i++){
        c = compress_mtf_encode_byte(byte_table, index_table, table_index, cin[i]);
_d(cin[i]);
_d(c);
        result[i] = c;
        if(c>MTF_UPDATE_TABLE_THRESH){
            table_index = ((table_index-1) & MASK_1_BYTE);
            compress_mtf_update_tables(byte_table, index_table, table_index, c);
        }

_mem_byte(byte_table);
_say("");
_mem_byte(index_table);
_die;

    }

    return nil;

}


//
//
char compress_mtf_encode_byte(char *byte_table, char *index_table, int curr_index, char c){ // compress_mtf_encode_byte#

    return index_table[(curr_index+c) & MASK_1_BYTE];

}


//
//
void compress_mtf_update_tables(char *byte_table, char *index_table, int curr_index, char c){ // compress_mtf_update_tables#

    char swap_val   = byte_table[curr_index];
    char swap_index = index_table[(int)c];

    byte_table[curr_index] = c;
    byte_table[(int)swap_index] = swap_val;

    index_table[(int)c]        = curr_index;
    index_table[(int)swap_val] = swap_index;

}


